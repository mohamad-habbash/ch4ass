public class modeltable {
    int id ;
    String name, major;	
    double grade ;

    public modeltable() {
    }
    
    
    public modeltable(int id, double grade, String name, String major) {
        this.id = id;
        this.grade = grade;
        this.name = name;
        this.major = major;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public double getGrade() {
        return grade;
    }

    public void setGrade(double grade) {
        this.grade = grade;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getMajor() {
        return major;
    }

    public void setMajor(String major) {
        this.major = major;
    }




}
